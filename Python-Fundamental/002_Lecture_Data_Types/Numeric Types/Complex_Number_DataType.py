'''Complex Number Data Type is used to store complex numbers. It is a number with a real and imaginary part. It can be positive or negative with no limit to the length of the number. '''

complex_num = 1 + 2j
print(complex_num)
print(type(complex_num))


negative_complex_num = -1 - 2j
print(negative_complex_num)
print(type(negative_complex_num))

calculation = 1 + 2j + 3 + 4j
print(calculation)
print(type(calculation))